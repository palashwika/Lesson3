#Assigning Different variables
name="Ashwika"
age=15
dancer=True
weight=10.6

#Printing Different Variables and their Data Type
print("Name:",name)
print("Data Type of Age is", type(name))

print("Age:", age)
print("Data type of age is", type(age))

print("dancer:", dancer)
print("Data Type of dancer is", type(dancer))

print("Weight:",weight)
print("Data Type of weight is", type(weight))

#Type casting to convert the datatype of variables
print("\n After Type Casting ....")
age=str(age)
print(age)
print("Data Type of age is", type(age))
weight = int(weight)
print(weight)
print("Data Type of Weight is",type(weight))